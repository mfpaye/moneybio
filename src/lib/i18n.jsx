const I18nContext = createContext(null)

export function I18nProvider({ children }) {
  const [locale, setLocaleState] = useState(() => localStorage.getItem('moneybio_locale') || 'en')
  function setLocale(lang) { setLocaleState(lang); localStorage.setItem('moneybio_locale', lang) }
  function t(key) { return T[locale]?.[key] || T['en']?.[key] || key }
  return <I18nContext.Provider value={{ locale, setLocale, t, locales: LOCALES }}>{children}</I18nContext.Provider>
}

export function useT() {
  const ctx = useContext(I18nContext)
  if (!ctx) throw new Error('useT must be inside I18nProvider')
  return ctx.t
}

export function useLocale() {
  const ctx = useContext(I18nContext)
  if (!ctx) throw new Error('useLocale must be inside I18nProvider')
  return { locale: ctx.locale, setLocale: ctx.setLocale, locales: ctx.locales }
}
